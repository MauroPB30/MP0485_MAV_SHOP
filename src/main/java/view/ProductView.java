    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();

        try {

            if (option == 2) { // Ańadir producto
                double price = Double.parseDouble(txtPrice.getText());
                int stock = Integer.parseInt(txtStock.getText());

                if (shop.addProduct(name, price, stock)) {
                    JOptionPane.showMessageDialog(this, "Producto ańadido");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Producto ya existe", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else if (option == 3) { // Ańadir stock
                int stock = Integer.parseInt(txtStock.getText());

                if (shop.addStock(name, stock)) {
                    JOptionPane.showMessageDialog(this, "Stock actualizado");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Producto no existe", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else if (option == 9) { // Eliminar producto

                if (shop.deleteProduct(name)) {
                    JOptionPane.showMessageDialog(this, "Producto eliminado");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Producto no existe", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos inválidos", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnOkActionPerformed

